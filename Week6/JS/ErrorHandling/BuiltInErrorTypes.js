/* Built-in Error Types

JavaScript provides several specialized error classes:

Error Class     When It Occurs

EvalError       Errors in the global eval() function
RangeError      Numeric values out of allowable range
ReferenceError  Accessing an undefined variable
SyntaxError     Paring code with incorrect syntax
TypeError       Invalid operations on a value/type
URIError        Malformed URI functions (decodeURI, etc.)

You can catch and distinguish these by checking err.name or err instanceof. */
