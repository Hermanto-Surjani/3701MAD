/* Setting Up Lodash

You can include Lodash in your project either by installing it through npm for Node.js environments or by
including it directly in your HTML for browser-based projects.

For Node.js:
    1. Install Lodash via npm: */

// npm install lodash

// For ES6 modules, you can install the ES module version of Lodash:

// npm install lodash-es

/*  2. Import Lodash in your JavaScript file: */

const _ = require("lodash");

/*  For Browser:

Include Lodash from a CDN in your HTML file: */

<script src="https://cdn.jsdelivr.net/npm/lodash@4.17.21/lodash.min.js"></script>;
