// Template Strings
// Basic Example:
const name = "Alice";
const greeting = `Hello, ${name}!`;
console.log(greeting); // Output: Hello, Alice!

// Multi-line string using template literals:
const multiLineString = `This is
 multi-line
string.`;
console.log(multiLineString);
/* Output:
This is
a multi-line
string.
*/

