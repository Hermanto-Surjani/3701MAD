/* Introduction to Control Structures

Control structures are fundamental programming constructs that determine the flow of execution in a
program. They allow developers to execute specific blocks of code based on certain conditions or to repeat
actions multiple times.

if / else Statements
The if statement is used to execute a block of code if a specified condition is true. The else statement
can be used to execute a different block of code if the condition is false.
Syntax: */

// if (condition) {
// // Code to execute if condition is true
// } else {
// // Code to execute if condition is false
// }

const age = 18;
if (age >= 18) {
  console.log("You are an adult.");
} else {
  console.log("You are a minor.");
}
// Output: You are an adult.
