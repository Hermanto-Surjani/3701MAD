// Ternary Operator

// The ternary operator is a concise way to perform conditional evaluations. It serves as a shorthand for
// if...else statements and is particularly useful for simple conditions.
// Syntax:

// condition ? expressionIfTrue : expressionIfFalse;

// Example:

const isMember = true;
const discount = isMember ? "20%" : "5%";
console.log(`Discount: ${discount}`);
// Output: Discount: 20%
