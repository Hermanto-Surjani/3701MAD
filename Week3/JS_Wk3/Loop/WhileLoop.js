// while Loop
// The while loop executes a block of code as long as the specified condition is true. It's useful when the
// number of iterations is not known beforehand.
// Syntax:
// while (condition) {
// // Code to execute
// }
// Example:

let count = 0;
while (count < 3) {
  console.log(`Count is ${count}`);
  count++;
}
// Output:
// Count is 0
// Count is 1
// Count is 2
