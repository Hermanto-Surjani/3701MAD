/* Summary

Control Structures: Essential for directing the flow of execution based on conditions and for
performing repetitive tasks.

if / else Statements:
    Allow conditional execution of code blocks.
    else if chains enable multiple condition checks.

Ternary Operator:
    Provides a concise syntax for simple conditional expressions.
    Enhances code readability for straightforward conditions.

Looping Structures:
    for Loop: Ideal for known iteration counts.
    for...in Loop: Useful for iterating over object properties or array indices.
    for...of Loop: Best for iterating over iterable objects like arrays and strings.
    while Loop: Suitable when the number of iterations isn't predetermined.

switch Statement:
    Provides a cleaner alternative to multiple if...else statements when checking against
    multiple conditions.

break and continue:
    break: Exits the current loop entirely.
    continue: Skips the current iteration and proceeds to the next one.
    
Understanding and effectively utilizing these control structures are fundamental skills in JavaScript
programming, enabling the creation of dynamic and responsive applications. */
